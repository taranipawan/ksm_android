public class GurpurabModal {

    private String sno;
    private String description;

    public GurpurabModal(String sno, String description) {
        this.sno = sno;
        this.description = description;
    }

    public String getSno() {
        return sno;
    }

    public String getDescription() {
        return description;
    }
}
